package ex_16_Array;

public class Lab_149_use_of_loop_In_Array {
    public static void main(String[] args) {

        int[] Marks={92,76,78,90,61,56};
        System.out.println(Marks.length);

        System.out.println("-----");

        for (int i=0;i<Marks.length;i++){

            System.out.println(Marks[i]);
        }


    }
}
