package ex_04_Operators;

public class Lab_31_Arthimetic_Operator {
    public static void main(String[] args) {

        int ab= 70;
        int bc = 89;
        float cd= 3.15f;
        // Arithmetic Operators
        //        + (Addition)
        //       - (Subtraction)
        //       * (Multiplication)
        //       / (Division)
        //       % (Modulus) | Modulus - Remainder

        System.out.println(ab*bc);
        System.out.println(ab+cd);
        System.out.println(ab-bc);
        System.out.println(bc%cd);
        System.out.println("ab+bc+cd");

    }
}
